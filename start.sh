# Default port
PORT=${1:-3001}

# Step 1: Install Dependencies
echo "?? Step 1: Dependencies yükleniyor..."
npm install --legacy-peer-deps
echo "? Dependencies yüklendi"
echo ""

# Step 2: Check .env.local
if [ ! -f .env.local ]; then
    echo "?? Step 2: .env.local oluţturuluyor..."
    echo "PORT=$PORT" > .env.local
    echo "NEXT_PUBLIC_SITE_URL=http://localhost:$PORT" >> .env.local
    echo "? .env.local oluţturuldu"
    echo ""
fi

# Step 3: Run Development Server
echo "?? Step 3: Development server baţlatýlýyor..."
echo "   ?? Port: $PORT"
echo "   ?? Tarayýcýda açýn: http://localhost:$PORT"
echo ""
echo "??  Baţka bir port'ta açmak için: ./start.sh 3002"
echo ""

if [ "$PORT" == "3000" ]; then
    npm run dev
elif [ "$PORT" == "3001" ]; then
    npm run dev:3001
elif [ "$PORT" == "3002" ]; then
    npm run dev:3002
elif [ "$PORT" == "3003" ]; then
    npm run dev:3003
elif [ "$PORT" == "8080" ]; then
    npm run dev:8080
else
    PORT=$PORT npm run dev:port
fi

