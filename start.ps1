# Bu script projeyi hýzlý ţekilde çalýţtýrmaya hazýrlar

Write-Host "??????????????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host "?  ???  ÖNCÜ Intelligence - Quick Start                          ?" -ForegroundColor Cyan
Write-Host "?  Modern Web Standards ile Geliţtirilmiţ Landing Page          ?" -ForegroundColor Cyan
Write-Host "??????????????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host ""

# Port selection
$port = if ($args.Count -gt 0) { $args[0] } else { 3001 }

# Step 1: Install Dependencies
Write-Host "?? Step 1: Dependencies yükleniyor..." -ForegroundColor Yellow
npm install --legacy-peer-deps
Write-Host "? Dependencies yüklendi" -ForegroundColor Green
Write-Host ""

# Step 2: Update .env.local
Write-Host "?? Step 2: Environment konfigürasyonu..." -ForegroundColor Yellow
if (-Not (Test-Path .env.local)) {
    Write-Host "   .env.local oluţturuluyor..." -ForegroundColor Gray
    "PORT=$port" | Out-File -FilePath .env.local -Encoding UTF8
    "NEXT_PUBLIC_SITE_URL=http://localhost:$port" | Add-Content .env.local
    Write-Host "? .env.local oluţturuldu" -ForegroundColor Green
} else {
    Write-Host "   .env.local zaten mevcut" -ForegroundColor Gray
}
Write-Host ""

# Step 3: Run Development Server
Write-Host "?? Step 3: Development server baţlatýlýyor..." -ForegroundColor Yellow
Write-Host "   ?? Port: $port" -ForegroundColor Cyan
Write-Host "   ?? Tarayýcýda açýn: http://localhost:$port" -ForegroundColor Cyan
Write-Host ""
Write-Host "??  Baţka bir port'ta açmak için: .\\start.ps1 3002" -ForegroundColor Gray
Write-Host ""

# Run appropriate command based on port
switch ($port) {
    3000 { npm run dev }
    3001 { npm run dev:3001 }
    3002 { npm run dev:3002 }
    3003 { npm run dev:3003 }
    8080 { npm run dev:8080 }
    default { 
        $env:PORT = $port
        npm run dev:port
    }
}

# Show additional commands
